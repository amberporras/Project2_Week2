package ilknur_Y;

public class SwapNumbers {
    public static void main(String[] args) {

        int x = 100, y = 200;
        System.out.println("x = " + x);
        System.out.println("y = " + y);

        int temp = x;
        x=y;
        y = temp;

        System.out.println(" ");

        System.out.println("x = " + x);
        System.out.println("y = " + y);
    }

}
